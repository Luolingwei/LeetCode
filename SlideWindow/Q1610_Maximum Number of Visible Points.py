import math

# 思路: 将每个点转化为到圆点的角度值, 排序后sliding window计算间隔angle的最多点数
# 注意: atan2只能计算一个角度值, 例如 -135, 135, 在坐标系中每一个角度有正负两种表示, 所以需要添加相应的角度到degree中

class Solution:
    def visiblePoints(self, points, angle, location):
        ox, oy = location[0],location[1]
        plus,res,degree = 0,0,[]
        for p in points:
            if p==location: plus+=1
            else: degree.append(math.degrees(math.atan2(p[1]-oy, p[0]-ox)))
        degree = degree + [d+360 if d<0 else d-360 for d in degree]
        degree.sort()
        l = 0
        for r,d in enumerate(degree):
            while d - degree[l] > angle:
                l += 1
            res = max(res, r-l+1)
        return res+plus


